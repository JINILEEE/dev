package homework;

public class Run02 {

	public static void main(String[] args) {
		
		/* 
		정수형 변수 a, b를 만든다
		a가 더 크면 “a가 더 크다.” 라고 출력
		b가 더 크면 “b가 더 크다.” 라고 출력
		a와 b가 같으면, “같다” 라고 출력
		*/

		int a = 6;
		int b = 1;

		if (a>b) {
		System.out.println("a가 더 크다.");
		} else if (a<b) {
		System.out.println("b가 더 크다.");
		} else {
		System.out.println("같다.");

		}

	}

}
